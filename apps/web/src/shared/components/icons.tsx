// Ícones de linha (Feather-style) compartilhados — tarefa 4.4. Consolida os
// glifos genuinamente duplicados entre `_shell/icons.tsx` e os ~9 fábricas de
// ícone bespoke por página (`lancamentos`, `acerto`, `pessoas`, `cambistas`,
// `configuracoes`, `premios`, `perfil`) — Search, Check, X, Edit, Trash,
// Calendar, Print, Clock, ChevronLeft/Right, Audit, Expand aparecem em 2+
// arquivos com o mesmo desenho. Ícones usados por uma única página (ex.:
// `IcoTicket`, `IcoWallet`, `IcoZap`) permanecem locais ao módulo até um
// segundo consumidor real justificar a promoção (`web-component-ownership`).
//
// Padrão: componente parametrizado (`size`/props do SVG), não um `ReactNode`
// pré-instanciado — o anti-padrão apontado na auditoria (`lancamentos/_components/icons.tsx`
// exportava `svg(size, children)` já resolvido, impedindo redimensionar/estilizar
// por uso).
export type IconProps = SVGProps<SVGSVGElement> & { size?: number }

function Svg({ size = 18, children, ...props }: IconProps & { children: React.ReactNode }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      {...props}
    >
      {children}
    </svg>
  )
}

